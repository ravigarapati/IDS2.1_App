export const userRole = async () => {
  let userType = '';
  await Auth.currentAuthenticatedUser().then(userData => {
    if (userData.attributes['custom:role'] === Enum.roles.homeowner) {
      userType = 'HomeOwner';
    } else if (userData.attributes['custom:role'] === Enum.roles.contractor) {
      userType = 'Contractor';
    } else if (
      userData.attributes['custom:role'] === Enum.roles.contractorPowerUser
    ) {
      userType = 'ContractorPowerUser';
    }
  });
  return userType;
};

